// ── Ranger policy templates ───────────────────────────────────────────────────
function buildRangerPolicy(request) {
  const dataset = DATASETS.find(d => d.id === request.datasetId);
  const schema = DATASET_SCHEMAS[request.datasetId];
  if (!dataset || !schema) return null;

  const permMap = {
    'read':       ['select'],
    'read-write': ['select', 'update', 'insert'],
    'api':        ['select', 'execute'],
    'bulk':       ['select', 'export'],
  };

  return {
    policyId: 'RGR-' + Math.random().toString(36).slice(2, 8).toUpperCase(),
    policyName: `${dataset.provider}_${request.department}_${request.accessType}_access`.toLowerCase().replace(/\s+/g, '_'),
    policyType: 'Access',
    serviceType: 'hive',
    serviceName: schema.database,
    createdAt: new Date().toISOString(),
    expiresAt: calcExpiry(request.duration),
    status: 'Active',
    resources: {
      database: schema.database,
      tables: schema.tables.map(t => t.name),
      columns: ['*'],
    },
    principals: {
      users: [request.requester.toLowerCase().replace(' ', '.') + '@corp.com'],
      groups: [`dept_${request.department.toLowerCase()}`],
      roles: [`role_${request.accessType}`],
    },
    permissions: permMap[request.accessType] || ['select'],
    conditions: [
      { type: 'row_filter', expression: `department = '${request.department}'` },
      { type: 'data_mask', columns: ['ssn', 'account_number', 'email'], maskType: 'MASK' },
    ],
    auditEnabled: true,
    approvedBy: request.approver,
    requestId: request.id,
  };
}

function calcExpiry(duration) {
  const d = new Date();
  const map = { '1month': 1, '3months': 3, '6months': 6, '12months': 12 };
  if (duration === 'permanent') return 'Never';
  d.setMonth(d.getMonth() + (map[duration] || 6));
  return d.toISOString();
}

